package projectta;

public class Obat {
    int id;
    
    String nama;
    String jumlah;
    String harga;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id=id;
    }
}